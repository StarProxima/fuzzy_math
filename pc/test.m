% Загрузка FIS
fis = readfis('PCSelection.fis');

% Тестовые данные
% Каждый ряд представляет собой один тестовый случай в формате:
% [ProcessorSpeed, RAM, Price, Graphics]
testData = [
    3.0, 16, 1200, 7;  % Сценарий 1
    2.0, 8, 800, 4;    % Сценарий 2
    4.5, 32, 1500, 9;  % Сценарий 3
    1.5, 4, 500, 2;    % Сценарий 4
    3.5, 16, 1000, 6   % Сценарий 5
];

% Вычисление и вывод результатов
for i = 1:size(testData, 1)
    output = evalfis(fis, testData(i, :));
    fprintf('Тестовый случай %d: Уверенность в покупке = %.2f%%\n', i, output);
end
